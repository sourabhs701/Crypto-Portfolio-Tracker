    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = (String)Token.getSelectedItem();
        String date = year.getText();
        ApiReq req = new ApiReq();
        
        
        req.past_price(id,date);
        
        
        
        double invested = Double.parseDouble(amt.getText());
        double qty =invested/req.past_price;
        req.currentPrice(id);
        double profit = (qty*req.price);
        double increase = profit - invested;
        double per = increase / invested * 100;
        String s=String.valueOf(df.format(profit));
        if (profit> invested){
        lable.setText(s+" i.e "+df.format(per)+"% Gain");
        }else{
        lable.setText(s+" i.e "+df.format(per)+"% loss");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

