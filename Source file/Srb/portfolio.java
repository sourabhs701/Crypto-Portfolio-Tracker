    public void portfolo_table(){
        
       String query = "select token_name,sum(b_qty-s_qty) as total_qty,b_price from Sourabh group by token_name;";
      
       try{
           double a[]=new double[5];
       
         javax.swing.table.DefaultTableModel dtm=
                     (javax.swing.table.DefaultTableModel)jTable1.getModel();
             ApiReq req = new ApiReq();
             var rc=dtm.getRowCount();
             while (rc--!=0){
               dtm.removeRow(0);
             }
             ResultSet rs=Conn.st2.executeQuery(query);
             while(rs.next()){
            String token =rs.getString("token_name");
            Float qty = rs.getFloat("total_qty");
            Float price = rs.getFloat("b_price");
            
            req.currentPrice(token);
            var s=req.price;
            var diff = s-price;
            var gain = df.format(diff*qty);
            var amt = df.format(qty*s);
            
            Object o[]={token,qty,amt,gain};
           dtm.addRow(o);
           }   
             
       }catch(Exception ex){
           
    System.out.print("error :"+ ex);
       }
       }
    public void curr_bal(){
        Double sum = getSum(2);
       String sum1 = df.format(getSum(2));
       jLabel3.setText(sum1);
       
       Double per = (getSum(3)/sum)*100;
       if(per<=0){
           sum1 = df.format(per);
       Gain.setText(sum1+"% loss");
       }else{
           sum1 = df.format(per);
       Gain.setText(sum1+"% Gain");
       }
    }
   
    public Double getSum(int a)
    {
        Double sum = 0.0;
        
        for(int i = 0; i < jTable1.getRowCount(); i++)
        {
            sum = sum + Double.parseDouble(jTable1.getValueAt(i, a).toString());
           
        }
        
        return sum;
    }


